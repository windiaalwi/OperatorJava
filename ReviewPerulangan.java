
package reviewperulangan;

public class ReviewPerulangan {

    public static void main(String[] args) {

        //while 
        int y = 200;
        while (y >= 0) {
            System.out.print(y + ",");
            y--;
        }

        //do while
        int z = 200;
        do {
            System.out.print(z + ",");
            z--;

        } while (z >= 0);

    }

}
